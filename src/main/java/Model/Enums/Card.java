package Model.Enums;

/**
 * Enum representing different types of spaceship components (cards) used in Galaxy Trucker.
 * Each card type can optionally represent specific functional roles such as cannons or engines.
 */
public enum Card {
    CABIN,
    ENGINE,
    CANNON,
    BATTERY_COMPARTMENT,
    SHIELD_GENERATOR,
    CARGO_HOLD,
    STRUCTURAL_MODULE,
    ALIEN_LIFE_SUPPORT;

}